package com.example.unispanapp.Models;

public class Operators {

    public Integer terceroId;
    public String fullName;
    public String numDocument;
    public String codeEnterprise;

    public Integer getTerceroId() {
        return terceroId;
    }

    public void setTerceroId(Integer terceroId) {
        this.terceroId = terceroId;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getNumDocument() {
        return numDocument;
    }

    public void setNumDocument(String numDocument) {
        this.numDocument = numDocument;
    }

    public String getCodeEnterprise() {
        return codeEnterprise;
    }

    public void setCodeEnterprise(String codeEnterprise) {
        this.codeEnterprise = codeEnterprise;
    }
}
